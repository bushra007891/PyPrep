# Introduction to Programming and Python
'''Programming is like giving instructions to a computer to make it perform specific tasks.'''

# Types of Programming Languages
# High-Level Languages
'''These are closer to human languages. Examples include Python, Java, and C++. They are easier for humans to understand and write, but they need to be translated into machine code for the computer to execute.'''

# Low-Level Languages
'''These are closer to machine code, which the computer understands directly. Examples include Assembly Language. These are harder for humans to understand and write, but they are faster and more efficient for computers.'''

# Object-Oriented Programming (OOP)
'''In this type, you deal with "objects," which can represent real-world things like cars or dogs. Python is an object-oriented programming language. This means it supports creating objects and working with them.'''

# Procedural Programming
'''This is one of the simplest forms of programming. It focuses on step-by-step instructions to complete a task. In Python, you can write code this way too.'''

# Functional Programming
'''Here, the focus is on creating and using functions. Functions are like small programs that do specific tasks. Python also supports this style of programming.'''

# Characteristics of Python
# Easy to Read and Write
'''Python’s syntax is simple, making it easy for beginners to learn and write code. Its code almost looks like plain English!'''

# Versatile
'''Python can be used for many types of applications—web development, data analysis, machine learning, games, and more.'''

# Huge Community Support
'''Python has a large and active community. If you ever get stuck, there are many people and resources to help you.'''

# Portable
'''Python code can run on different platforms (Windows, Mac, Linux) without any changes.'''

# Open Source
'''Python is free to use, and its source code is open, meaning you can contribute to its development.'''
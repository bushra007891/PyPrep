# Understanding Operators in Python
'''In Python, operators are special symbols that are used to perform operations on variables and values.'''

# Types of Operators in Python
'''Arithmetic Operators
Comparison Operators
Assignment Operators
Logical Operators
Bitwise Operators
Membership Operators
Identity Operators'''

# Characteristics of Operators
# Perform Operations on Values:
'''Operators let you perform calculations, compare values, and manipulate data.'''

# Return Results:
'''Depending on the type of operator, they return either a value or a Boolean (True or False).'''

# Simplify Code:
'''Operators help you write concise and effective code.'''

# Used with Different Data Types:
'''Some operators work with numbers (arithmetic operators), while others work with strings or lists (membership operators).'''

# Combine Expressions:
'''Logical operators allow you to combine multiple conditions into a single expression'''

# Arithmetic Operators
'''These operators are used to perform basic mathematical operations like addition, subtraction, multiplication, and division.
Operator	Description	              Example
+	        Addition	              3 + 2 results in 5
-	        Subtraction	              5 - 2 results in 3
*	        Multiplication	          3 * 2 results in 6
/	        Division	              6 / 2 results in 3.0
%	        Modulus (remainder)	      5 % 2 results in 1
**	        Exponentiation	          2 ** 3 results in 8
//	        Floor division	          5 // 2 results in 2'''

# Comparison Operators
'''Comparison operators are used to compare two values and return either True or False.
Operator	Description	                   Example
==	        Equal to	                   5 == 5 is True
!=	        Not equal to	               5 != 3 is True
>	        Greater than	               5 > 3 is True
<	        Less than	                   3 < 5 is True
>=	        Greater than or equal to	   5 >= 3 is True
<=	        Less than or equal to	       3 <= 5 is True'''

# Assignment Operators
'''These operators are used to assign values to variables.
Operator	Description	                Example
=	        Assigns value	            x = 5
+=	        Adds and assigns	        x += 3 (same as x = x + 3)
-=	        Subtracts and assigns	    x -= 2
*=	        Multiplies and assigns	    x *= 2
/=	        Divides and assigns	        x /= 2
%=	        Modulus and assigns	        x %= 2'''

# Logical Operators
'''Logical operators are used to combine conditional statements. They return either True or False.
Operator	Description	                                    Example
and	        True if both conditions are true	            5 > 2 and 5 < 10 is True
or	        True if at least one condition is true	        5 > 2 or 5 > 10 is True
not	        Reverses the result	                            not (5 > 2) is False'''

# Bitwise Operators
'''These operators are used to perform operations on binary numbers.
Operator	        Description	             Example
&	                AND	                     5 & 3 results in 1
`	                `	                     OR
^	                XOR	                     5 ^ 3 results in 6
~	                NOT	                     ~5 results in -6
<<	                Left                     shift	5 << 1 results in 10
>>	                Right                    shift	5 >> 1 results in 2'''

# Membership Operators
'''Membership operators are used to test if a sequence contains a certain value.
Operator	            Description	                                    Example
in	                    True if value is in the sequence	            'a' in 'apple' is True
not in	                True if value is not in the sequence	        'x' not in 'apple' is True'''

# Identity Operators
'''Operator	            Description	                                                        Example
is	                    True if both variables point to the same object	                    x is y
is not	                True if both variables point to different objects	                x is not y'''

